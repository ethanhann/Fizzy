namespace Acme\Person;

use JMS\Serializer\Annotation AS JMS;

class PersonRequest
{
    /**
     * @JMS\Type("string")
     */
    private $name;

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }
}
