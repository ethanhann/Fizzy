namespace Acme\Company;


class Company
{
    private $name = 'Sprockets, Inc.';

    /**
     * @param $name
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
} 